package logic;


//import java.sql.Date;

public class Patient {

	private String description;
	private int birthday; //eig Date
	private String gender;
	private boolean absCareGivingState = false;
	
	public Patient(String description, int birthday, String gender,
			boolean absCareGivingState) {
		super();
		this.description = description;
		this.birthday = birthday;
		this.gender = gender;
		this.absCareGivingState = absCareGivingState;
	}	
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getBirthday() {
		return birthday;
	}
	public void setBirthday(int birthday) {
		this.birthday = birthday;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public boolean isAbsCareGivingState() {
		return absCareGivingState;
	}
	public void setAbsCareGivingState(boolean absCareGivingState) {
		this.absCareGivingState = absCareGivingState;
	}
	
	
	@Override
	public String toString() {
		return "Patient [description=" + description + ", birthday=" + birthday
				+ ", gender=" + gender + ", absCareGivingState="
				+ absCareGivingState + "]";
	}
	
}
